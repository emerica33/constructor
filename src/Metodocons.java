
public class Metodocons {
    public Metodocons (){
        System.out.println("Metodo sin parametros.");
    }
    public Metodocons (int a){
        System.out.println("Metodo con 1 parametros:"+a);
      
    }
    public Metodocons (int a, int b){
        System.out.println("Metodo con 2 parametros:"+a+", "+b);
        
    }
    public Metodocons (int a, int b, String nombre){
        System.out.println("Metodo con 3 parametros:"+a+", "+b+", "+nombre);

    }
    
}
